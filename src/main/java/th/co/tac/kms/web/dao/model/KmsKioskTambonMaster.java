package th.co.tac.kms.web.dao.model;

public class KmsKioskTambonMaster {
	private String tambonId;
	private String tambonName;
	private String tambonNameEn;

	public String getTambonId() {
		return tambonId;
	}

	public void setTambonId(String tambonId) {
		this.tambonId = tambonId;
	}

	public String getTambonNameEn() {
		return tambonNameEn;
	}

	public void setTambonNameEn(String tambonNameEn) {
		this.tambonNameEn = tambonNameEn;
	}

	public String getTambonName() {
		return tambonName;
	}

	public void setTambonName(String tambonName) {
		this.tambonName = tambonName;
	}

}
